							<h3 class="widget-title style12">Date : 5 Feb 2020<span class="ttg"></span></h3>
						
							<p>สำหรับรถยนต์ MG 3 ซึ่งเป็นรถที่เหมาะกับไลฟ์สไตล์การใช้งานของคนเมือง ที่ถึงแม้จะมีคุณสมบัติช่วยในการประหยัดน้ำมันอยู่แล้ว แต่วันนี้เราก็ได้นำเคล็ดลับของการขับรถเพื่อเป็นการประหยัดน้ำมันมาฝากกันเพิ่มเติมอีกด้วย โดยเป็นวิธีการง่ายๆ ที่ต้องใช้ความใส่ใจเพิ่มมากขึ้นและสามารถทำได้จริงในชีวิตประจำวัน</p>
							
							<p>1. เตรียมความพร้อมก่อนออกเดินทาง เพื่อเป็นการประหยัด และไม่เปลืองน้ำมันโดยใช่เหตุ นั่นคือ การศึกษาเส้นทางก่อนออกเดินทาง โดยเฉพาะอย่างยิ่งหากท่านต้องเดินทางไปยังสถานที่ที่ไม่คุ้นเคยแล้วล่ะก็ควรทำการศึกษาเส้นทางล่วงหน้าเพื่อไม่ให้หลงทาง ขับอ้อม ไปเจอรถติด ซึ่งทำให้เปลืองน้ำมันอีกทั้งยังเป็นการเสียเวลาอีกด้วย<br>
								2. เอาสัมภาระที่ไม่จำเป็นออกไปจากรถ เนื่องจากหากรถมีการบรรทุกน้ำหนักเยอะจะทำให้กินน้ำมันเชื้อเพลิง ข้อนี้เป็นข้อที่สามารถทำได้ง่าย ซึ่งนอกจากจะเป็นการประหยัดน้ำมันแล้ว ยังช่วยให้รถยนต์เอ็มจี ของคุณมีความเป็นระเบียบเรียบร้อยอีกด้วย<br>
								3. ตรวจเช็คลมยางอยู่เสมอ ให้อยู่ในระดับมาตรฐานที่กำหนดไว้ของรถยนต์ MG 3 เนื่องจากหากลมยางอ่อนจะทำให้กินน้ำมันมากขึ้น<br>
								4. ตรวจเช็คเครื่องยนต์อยู่เสมอ ไม่ว่าจะเป็น ไส้กรองอากาศ น้ำมันหล่อลื่น ระบบระบายความร้อน หัวเทียน เนื่องจากหากเครื่องยนต์มีความบกพร่องจะทำให้การเผาไหม้ของเชื้อเพลิงไม่มีประสิทธิภาพ ทำให้เครื่องยนต์กินน้ำมันมากขึ้นตามไปด้วย<br>
								5. ตรวจเช็คเครื่องปรับอากาศ ให้อยู่ในสภาวะปกติที่ทำงานได้อย่างมีประสิทธิภาพ เพื่อไม่ให้ระบบปรับอากาศทำงานหนักซึ่งจะทำให้กินน้ำมันซึ่งเป็นพลังงานเชื้อเพลิงมากขึ้น<br>
								6. เติมน้ำมันที่สามารถใช้เติมรถยนต์ MG 3ที่มีราคาประหยัดที่สุด จะช่วยให้สามารถประหยัดเงินค่าน้ำมันได้
							</p>
                           
                            <br>
							<p>นอกจากวิธีการเตรียมความพร้อมและการตรวจเช็คสภาพของรถยนต์ MG ให้พร้อมสำหรับการใช้งานและเป็นการประหยัดน้ำมันแล้ว ยังมีเทคนิคต่างๆ ในการขับขี่ที่สามารถช่วยประหยัดน้ำมันได้อีกด้วย ได้แก่></p>
                            <p>
							1. ไม่ควรเร่งเครื่องขณะที่รถจอดอยู่นิ่งๆ เพราะจะทำให้เปลืองน้ำมันโดยเปล่าประโยชน์<br>
								2. ขับด้วยความเร็วสม่ำเสมอ หรือความเร็วคงที่ ซึ่งการขับรถที่ความเร็ว 90 กิโลเมตรต่อชั่วโมงจะช่วยให้สามารถประหยัดน้ำมันได้มากที่สุด<br>
								3. ไม่ขับรถด้วยความเร็วที่สูงมาก เนื่องจากแรงต้านอากาศจะทำให้รถต้องใช้พลังงานเชื้อเพลิงมากกว่าปกติ ทำให้กินน้ำมันมากกว่าการขับรถด้วยความเร็วที่อยู่ในช่วง 80-100 กิโลเมตรต่อชั่วโมง<br>
								4. ไม่แตะเบรกอย่างรุนแรงเพราะจะทำให้เปลืองน้ำมัน<br>
								5. สำหรับรถเกียร์ธรรมดาไม่ควรเหยียบคลัชต์แช่ เนื่องจากการเหยียบคลัชต์แช่จะทำให้เปลืองน้ำมันมากกว่าปกติ<br>
								6. ไม่ควรเร่งเครื่องให้มีรอบสูงขณะออกรถ และสำหรับเกียร์ธรรมดาไม่ควรขับแบบลากเกียร์ เพราะจะทำให้เปลืองน้ำมันและทำให้เกียร์เสื่อมสภาพเร็วกว่าปกติอีกด้วย
							</p>
